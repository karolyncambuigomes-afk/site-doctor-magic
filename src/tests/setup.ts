import { beforeAll, afterAll, afterEach } from 'vitest';
import { cleanup } from '@testing-library/react';
import '@testing-library/jest-dom';

// Global test setup

// Cleanup after each test case
afterEach(() => {
  cleanup();
});

// Mock window.matchMedia
